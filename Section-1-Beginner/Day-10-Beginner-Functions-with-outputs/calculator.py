# Add function
def add(n1, n2):
    return n1 + n2


# Sub function
def sub(n1, n2):
    return n1 - n2


# Multiply function
def mul(n1, n2):
    return n1 * n2


# Divide function
def div(n1, n2):
    return n1 / n2


